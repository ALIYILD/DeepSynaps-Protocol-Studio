const I18N_KEY = 'ds_locale';
let _currentLocale = localStorage.getItem(I18N_KEY) || 'en';

export function t(key, vars) {
  const locale = TRANSLATIONS[_currentLocale] || TRANSLATIONS.en;
  let str = locale[key] ?? TRANSLATIONS.en[key];
  if (str === undefined || str === null) {
    // Guard: never render a raw i18n key in the UI.
    // In dev builds, warn so missing keys are caught early.
    // In production, derive a human-readable fallback from the key's last segment.
    if (import.meta.env && import.meta.env.DEV) {
      console.warn('[i18n] missing key:', key, '(locale:', _currentLocale, ')');
    }
    str = key.split('.').pop().replace(/[-_]/g, ' ').replace(/\b\w/g, c => c.toUpperCase());
  }
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), v);
    }
  }
  return str;
}

export function setLocale(code) {
  if (!TRANSLATIONS[code]) return;
  _currentLocale = code;
  localStorage.setItem(I18N_KEY, code);
  window._i18nLocale = code;
  // RTL support
  document.documentElement.setAttribute('lang', code);
  document.documentElement.setAttribute('dir', code === 'ar' ? 'rtl' : 'ltr');
  // Re-render nav labels using data-i18n attributes
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const k = el.getAttribute('data-i18n');
    el.textContent = t(k);
  });
  // Dispatch event so app.js can re-render nav if needed
  window.dispatchEvent(new CustomEvent('ds:locale-changed', { detail: { locale: code } }));
}

export function getLocale() { return _currentLocale; }

