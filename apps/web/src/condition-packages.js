// ─── Derived helpers ──────────────────────────────────────────────────────────

export const PACKAGES_BY_ID = Object.fromEntries(
  CONDITION_PACKAGES.map(p => [p.id, p])
);

export function getPackagesByTier(tier) {
  return CONDITION_PACKAGES.filter(p => p.tier === tier);
}

export function getPackagesByCategory(category) {
  return CONDITION_PACKAGES.filter(p => p.category === category);
}

export function getPackageStatus(id) {
  return PACKAGES_BY_ID[id]?.status ?? null;
}

export function calcCompletionScore(pkg) {
  const dims = ['schema','protocols','assessments','handbook','patientView','qaComplete'];
  let score = 0;
  for (const d of dims) {
    const v = pkg.status[d];
    if (v === true) score += 1;
    else if (v === 'partial') score += 0.5;
  }
  return score; // out of 6
}

export function calcCompletionPct(pkg) {
  return Math.round((calcCompletionScore(pkg) / 6) * 100);
}

export function getSummaryStats() {
  const total = CONDITION_PACKAGES.length;
  let qaComplete = 0, fullyReady = 0, schemaReady = 0, protocolsReady = 0;
  let handbookReady = 0, patientViewReady = 0;
  for (const p of CONDITION_PACKAGES) {
    if (p.status.qaComplete === true)    qaComplete++;
    if (p.status.schema === true)        schemaReady++;
    if (p.status.protocols === true)     protocolsReady++;
    if (p.status.handbook === true)      handbookReady++;
    if (p.status.patientView === true)   patientViewReady++;
    if (calcCompletionScore(p) === 6)    fullyReady++;
  }
  return { total, qaComplete, fullyReady, schemaReady, protocolsReady, handbookReady, patientViewReady };
}

// ─── localStorage status overrides (for clinician QA workflow) ────────────────
const LS_KEY = 'ds_condition_pkg_status';

export function loadStatusOverrides() {
  try { return JSON.parse(localStorage.getItem(LS_KEY) || '{}'); } catch { return {}; }
}

export function saveStatusOverride(conditionId, dimension, value) {
  const overrides = loadStatusOverrides();
  if (!overrides[conditionId]) overrides[conditionId] = {};
  overrides[conditionId][dimension] = value;
  localStorage.setItem(LS_KEY, JSON.stringify(overrides));
}

export function getMergedStatus(pkg, overrides) {
  const ov = overrides[pkg.id] ?? {};
  return { ...pkg.status, ...ov };
}
