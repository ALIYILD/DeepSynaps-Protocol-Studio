function _escHtml(s) {
  return String(s ?? '')
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;');
}

function _toneStyle(tone) {
  const palette = {
    amber: { bg: 'rgba(245, 158, 11, 0.08)', border: 'rgba(245, 158, 11, 0.35)', fg: 'var(--amber, #f59e0b)' },
    slate: { bg: 'rgba(148, 163, 184, 0.08)', border: 'rgba(148, 163, 184, 0.28)', fg: 'var(--text-secondary, #64748b)' },
    teal: { bg: 'rgba(0, 212, 188, 0.08)', border: 'rgba(0, 212, 188, 0.25)', fg: 'var(--teal, #00d4bc)' },
  }[tone] || { bg: 'rgba(245, 158, 11, 0.08)', border: 'rgba(245, 158, 11, 0.35)', fg: 'var(--amber, #f59e0b)' };
  return palette;
}

function _renderDisclaimerCard(cfg, opts = {}) {
  const tone = _toneStyle(cfg.tone || 'amber');
  const compact = !!opts.compact;
  const body = Array.isArray(cfg.body) ? cfg.body : [cfg.body].filter(Boolean);
  const bodyHtml = body.map((line) => `<div style="margin-top:${compact ? 4 : 6}px;line-height:1.55">${_escHtml(line)}</div>`).join('');
  return `<div class="${opts.className || 'clinical-disclaimer-banner'}" role="${_escHtml(opts.role || 'note')}" aria-label="${_escHtml(opts.ariaLabel || cfg.title)}" style="
    background: ${tone.bg};
    border: 1px solid ${tone.border};
    border-radius: var(--radius-md, 8px);
    padding: ${compact ? '10px 12px' : '12px 16px'};
    margin-bottom: ${opts.marginBottom || 16}px;
    display: flex;
    gap: 12px;
    align-items: flex-start;
    font-size: ${compact ? '12px' : '13px'};
    line-height: 1.5;
  ">
    <div style="flex-shrink: 0; padding-top: 2px; color: ${tone.fg}; font-weight: 700;">AI</div>
    <div>
      <strong>${_escHtml(cfg.title)}</strong>
      ${bodyHtml}
      ${cfg.footer ? `<div style="margin-top:${compact ? 4 : 8}px;color:${tone.fg};font-size:${compact ? '11px' : '12px'}">${_escHtml(cfg.footer)}</div>` : ''}
    </div>
  </div>`;
}

function _resolveDisclaimer(kind = 'global') {
  const key = String(kind || 'global').toLowerCase();
  return DISCLAIMER_COPY[key] || DISCLAIMER_COPY.global;
}

/**
 * Render the required full clinical disclaimer banner.
 * Must appear on every AI/analyzer page.
 * 
 * @returns {string} HTML for the banner
 */
export function renderClinicalDisclaimer(kind = 'global') {
  return _renderDisclaimerCard(_resolveDisclaimer(kind), {
    className: 'clinical-disclaimer-banner',
    role: 'region',
    ariaLabel: 'Clinical disclaimer',
  });
}

/**
 * AI output disclaimer for reports, summaries, scores, and draft outputs.
 * Use compact mode when rendered beside a result block.
 */
export function renderAiOutputDisclaimer(options = {}) {
  const variant = options.patientFacing ? 'patient' : (options.variant || 'ai');
  return _renderDisclaimerCard(_resolveDisclaimer(variant), {
    className: options.className || 'ai-output-disclaimer',
    compact: options.compact !== false,
    marginBottom: options.marginBottom || 0,
  });
}

/**
 * Module-specific disclaimers used on DeepSynaps AI surfaces.
 */
export function renderModuleClinicalDisclaimer(moduleKey = 'global', options = {}) {
  return _renderDisclaimerCard(_resolveDisclaimer(moduleKey), {
    className: options.className || `module-disclaimer module-disclaimer-${String(moduleKey || 'global')}`,
    compact: options.compact !== false,
    marginBottom: options.marginBottom || 16,
  });
}

export function renderPatientFacingDisclaimer(options = {}) {
  return renderModuleClinicalDisclaimer('patient', options);
}

