function renderClinicalDisclaimer() {
  return _banner('global', GLOBAL_DISCLAIMER_TEXT, 'data-testid="global-clinical-disclaimer" ', 'Clinical disclaimer');
}

function renderAiOutputDisclaimer(opts = {}) {
  const variant = String(opts.variant || 'default').toLowerCase();
  const text = AI_OUTPUT_TEXT[variant] || AI_OUTPUT_TEXT.default;
  const testId = variant === 'patient'
    ? 'patient-ai-output-disclaimer'
    : variant === 'export'
      ? 'export-ai-output-disclaimer'
      : 'ai-output-disclaimer';
  return _banner('ai-output', text, `data-testid="${testId}" `, 'AI output disclaimer');
}

function renderModuleClinicalDisclaimer(moduleKey) {
  const key = String(moduleKey || 'default').toLowerCase();
  const text = MODULE_COPY[key] || MODULE_COPY.default;
  return _banner(`module-${key}`, text, `data-testid="module-disclaimer-${escHtml(key)}" `, `${key.toUpperCase()} disclaimer`);
}

function renderCitationLink(citation = {}) {
  const title = citation?.title || citation?.journal || 'Evidence source';
  const pmid = String(citation?.pmid || '').trim();
  const doi = String(citation?.doi || '').trim();
  if (pmid) {
    return `<a class="dt-cit-link" href="https://pubmed.ncbi.nlm.nih.gov/${escHtml(pmid)}/" target="_blank" rel="noopener noreferrer">${escHtml(citation.label || `PMID:${pmid}`)}</a>`;
  }
  if (doi) {
    return `<a class="dt-cit-link" href="https://doi.org/${escHtml(doi)}" target="_blank" rel="noopener noreferrer">${escHtml(citation.label || `DOI:${doi}`)}</a>`;
  }
  return `<span class="dt-cit-text">${escHtml(title)}</span>`;
}

function renderEvidenceList(citations) {
  if (!Array.isArray(citations) || !citations.length) return '';
  return `<ul class="dt-cit-list">${citations.map((c) => `<li>${renderCitationLink(c)}</li>`).join('')}</ul>`;
}

function renderNoEvidenceNotice(opts = {}) {
  const condition = opts.condition ? ` for ${escHtml(opts.condition)}` : '';
  const protocol = opts.protocol_name ? ` "${escHtml(opts.protocol_name)}"` : '';
  const detail = opts.detail ? `<div class="dt-notice-detail">${escHtml(opts.detail)}</div>` : '';
  return `<div class="dt-notice-no-evidence">
    <strong>No local evidence found${condition}${protocol}.</strong>
    Clinician judgment required before use.
    ${detail}
  </div>`;
}

function renderEvidenceDisclaimerBanner() {
  return `<div class="dt-notice-info"><strong>Evidence is decision-support only.</strong> Clinician review required before clinical use.</div>`;
}

function renderEvidenceSection(title, citations, opts = {}) {
  const expanded = !!opts.expanded;
  return `<details class="dt-evidence-section" ${expanded ? 'open' : ''}>
    <summary>${escHtml(title)}</summary>
    ${renderEvidenceList(citations) || renderNoEvidenceNotice({ detail: 'No citations available in the current workspace.' })}
  </details>`;
}

function renderEvidenceDisclaimerInline(opts = {}) {
  const confidence = opts.confidence_tier ? ` Confidence: ${escHtml(opts.confidence_tier)}.` : '';
  const clinician = opts.requires_clinician_review === false ? '' : ' Clinician review required.';
  return `<div class="dt-notice-inline">Evidence is decision-support only.${clinician}${confidence}</div>`;
}

function renderApprovalBadge(status, opts = {}) {
  const normalized = String(status || 'pending').toLowerCase();
  const labels = {
    approved: 'Approved',
    escalated: 'Escalated',
    pending: 'Awaiting approval',
  };
  const label = labels[normalized] || labels.pending;
  const link = opts.show_evidence_link && opts.evidence_link_href
    ? ` <a href="${escHtml(opts.evidence_link_href)}">view evidence</a>`
    : '';
  return `<span class="dt-approval-badge dt-approval-badge--${escHtml(normalized)}">${escHtml(label)}${link}</span>`;
}

function renderClinicalDisclaimerLegacy() {
  return renderClinicalDisclaimer();
}

