function renderEvidenceLink(opts) {
  const { pubmed, doi, label } = opts || {};
  if (!pubmed && !doi) return '';
  const href = pubmed
    ? `https://pubmed.ncbi.nlm.nih.gov/${escHtml(pubmed)}/`
    : `https://doi.org/${escHtml(doi)}`;
  const text = label || (pubmed ? `PMID: ${pubmed}` : `DOI: ${doi}`);
  return `<a href="${href}" target="_blank" rel="noopener noreferrer" style="color:var(--link-color, #3b82f6);text-decoration:underline">${escHtml(text)}</a>`;
}

