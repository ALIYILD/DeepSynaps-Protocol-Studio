export function searchProtocols(query, filters = {}) {
  let results = PROTOCOL_LIBRARY;
  if (filters.conditionId) results = results.filter(p => p.conditionId === filters.conditionId);
  if (filters.device)      results = results.filter(p => p.device === filters.device);
  if (filters.type)        results = results.filter(p => p.type === filters.type);
  if (filters.evidenceGrade) results = results.filter(p => p.evidenceGrade === filters.evidenceGrade);
  if (filters.governance)  results = results.filter(p => (p.governance || []).includes(filters.governance));
  if (query) {
    const q = query.toLowerCase();
    results = results.filter(p =>
      (p.name || '').toLowerCase().includes(q) ||
      (p.target || '').toLowerCase().includes(q) ||
      (p.notes || '').toLowerCase().includes(q) ||
      (p.tags || []).some(t => t.includes(q)) ||
      (p.conditionId || '').replace(/-/g,' ').includes(q)
    );
  }
  return results;
}

