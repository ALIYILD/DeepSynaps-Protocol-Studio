class ProtocolReportGenerator:
    """
    Generates clinician-ready neuromodulation protocol reports.

    Produces JSON (API-ready), Markdown (display/print), and SOAP-format
    clinical notes (EHR-ready) from patient profiles and recommended protocols.

    Usage::
        generator = ProtocolReportGenerator()
        report = generator.generate_report(
            patient_id="PT-001",
            protocols=[...],
            patient_profile={"age": 45, "sex": "female", "diagnosis": "MDD"},
            format="full"
        )
        md = generator.generate_markdown(report)
        json_str = generator.generate_json(report)
        soap = generator.generate_clinical_note(report)
    """

    def __init__(self) -> None:
        self._evidence_db = EVIDENCE_DATABASE
        self._contra_db = CONTRAINDICATIONS_DB
        self._version = __version__
        self._system_name = SYSTEM_NAME

    # ------------------------------------------------------------------
    # Public API
    # ------------------------------------------------------------------

    def generate_report(
        self,
        patient_id: str,
        protocols: List[Dict[str, Any]],
        patient_profile: Dict[str, Any],
        report_format: str = "full",
    ) -> Dict[str, Any]:
        """
        Generate a comprehensive protocol report.

        Args:
            patient_id: Unique patient identifier (e.g., "PT-001").
            protocols: List of protocol dictionaries from the Protocol Generator.
            patient_profile: Dictionary with keys: age, sex, diagnosis,
                             comorbidities (optional), medications (optional),
                             prior_treatments (optional), contraindications (optional).
            report_format: One of "full", "summary", or "patient_facing".

        Returns:
            A dictionary containing the complete report structure.

        Raises:
            InvalidInputError: If required fields are missing or invalid.
            FormatNotSupportedError: If report_format is not recognized.
        """
        fmt = self._parse_format(report_format)
        profile = self._build_profile(patient_id, patient_profile)
        profile.validate()

        report_id = self._generate_report_id(patient_id)
        generated_at = datetime.utcnow().strftime("%Y-%m-%dT%H:%M:%SZ")
        next_review = (datetime.utcnow() + timedelta(days=30)).strftime("%Y-%m-%d")

        # Build protocol recommendations with evidence lookup
        recommendations = self._build_recommendations(protocols, profile)

        # Check contraindications and generate safety warnings
        safety_warnings = self._check_safety(protocols, profile)

        # Calculate overall evidence quality
        evidence_quality = self._calculate_evidence_quality(recommendations)

        # Calculate overall confidence
        confidence_overall = self._calculate_overall_confidence(recommendations)

        report: Dict[str, Any] = {
            "report_id": report_id,
            "generated_at": generated_at,
            "generated_by": self._system_name,
            "version": self._version,
            "format": fmt.value,
            "patient": profile.to_dict(),
            "recommended_protocols": [r.to_dict() for r in recommendations],
            "safety_warnings": [w.to_dict() for w in safety_warnings],
            "evidence_quality": evidence_quality,
            "disclaimer": DEFAULT_DISCLAIMER,
            "research_only_notice": RESEARCH_ONLY_NOTICE if RESEARCH_ONLY_FLAG else None,
            "next_review_date": next_review,
            "confidence_overall": round(confidence_overall, 2),
            "research_only_notes": 1 if RESEARCH_ONLY_FLAG else 0,
            "data_sources_consulted": self._count_data_sources(),
        }

        # Apply format-specific transformations
        report = self._apply_format(report, fmt)

        return report

    def generate_markdown(self, report: Dict[str, Any]) -> str:
        """
        Convert a report dictionary to Markdown format.

        Args:
            report: The report dictionary from generate_report().

        Returns:
            A Markdown-formatted string ready for display or printing.
        """
        lines: List[str] = []
        r = report
        is_patient = r.get("format") == "patient_facing"

        # Header
        lines.append(f"# Neuromodulation Protocol Report")
        lines.append(f"**Report ID:** {r['report_id']}")
        lines.append(f"**Generated:** {r['generated_at']}")
        lines.append(f"**System:** {r['generated_by']}")
        lines.append("")

        # Patient info
        lines.append("## Patient Information")
        p = r["patient"]
        lines.append(f"- **ID:** {p['id']}")
        lines.append(f"- **Age:** {p['age']}")
        lines.append(f"- **Sex:** {p['sex']}")
        lines.append(f"- **Diagnosis:** {p['diagnosis'].replace('_', ' ').title()}")
        if p.get("comorbidities"):
            lines.append(f"- **Comorbidities:** {', '.join(p['comorbidities'])}")
        if p.get("special_population"):
            lines.append(f"- **Special Population:** {p['special_population']}")
        lines.append("")

        # Safety warnings
        if r["safety_warnings"]:
            lines.append("## ⚠ Safety Warnings")
            for w in r["safety_warnings"]:
                emoji = {"none": "✓", "low": "⚠", "moderate": "⚠⚠",
                         "high": "🚫", "contraindicated": "⛔"}.get(w["level"], "⚠")
                lines.append(f"{emoji} **{w['level'].upper()}:** {w['message']}")
                if w.get("action_required"):
                    lines.append(f"   *Action:* {w['action_required']}")
            lines.append("")

        # Recommended protocols
        lines.append(f"## Recommended {'Treatments' if is_patient else 'Protocols'}")
        lines.append("")

        for proto in r["recommended_protocols"]:
            lines.append(f"### {proto['rank']}. {proto['protocol_name']}")
            lines.append("")
            lines.append(f"**{'Treatment Type' if is_patient else 'Modality'}:** {proto['modality']}")
            lines.append("")

            # Parameters
            lines.append("#### Parameters")
            params = proto["parameters"]
            for key, val in params.items():
                display_key = key.replace("_", " ").title()
                lines.append(f"- **{display_key}:** {val}")
            lines.append("")

            # Evidence
            if not is_patient and proto.get("evidence_summary"):
                lines.append(f"**Evidence:** {proto['evidence_summary']}")
                lines.append("")

            # Predicted response
            if proto.get("predicted_response"):
                lines.append(f"**Expected Outcome:** {proto['predicted_response']}")
                lines.append("")

            # Safety
            lines.append(f"**{'What to Expect' if is_patient else 'Safety Profile'}:** "
                         f"{proto['safety_summary']}")
            lines.append("")

            # Cost (clinician only)
            if not is_patient and proto.get("cost_estimate"):
                lines.append(f"**Estimated Cost:** {proto['cost_estimate']}")
                lines.append("")

            # References (clinician only)
            if not is_patient and proto.get("key_references"):
                lines.append("**Key References:**")
                for ref in proto["key_references"]:
                    lines.append(f"- {ref}")
                lines.append("")

            lines.append("---")
            lines.append("")

        # Evidence quality
        if not is_patient:
            lines.append(f"**Evidence Quality:** {r['evidence_quality']}")
            lines.append("")

        # Confidence
        if not is_patient:
            lines.append(f"**Overall Confidence:** {r['confidence_overall']:.0%}")
            lines.append("")

        # Review date
        lines.append(f"**Next Review Date:** {r['next_review_date']}")
        lines.append("")

        # Disclaimers
        lines.append("---")
        lines.append("")
        lines.append(f"*{r['disclaimer']}*")
        lines.append("")
        if r.get("research_only_notice"):
            lines.append(f"**🔬 {r['research_only_notice']}**")
            lines.append("")

        return "\n".join(lines)

    def generate_json(self, report: Dict[str, Any], indent: int = 2) -> str:
        """
        Serialize a report dictionary to a JSON string.

        Args:
            report: The report dictionary from generate_report().
            indent: JSON indentation level (default: 2).

        Returns:
            A pretty-printed JSON string.
        """
        try:
            return json.dumps(report, indent=indent, ensure_ascii=False, default=str)
        except (TypeError, ValueError) as exc:
            raise ProtocolReportError(f"JSON serialization failed: {exc}") from exc

    def generate_clinical_note(self, report: Dict[str, Any]) -> str:
        """
        Generate a SOAP-format clinical note ready for EHR pasting.

        Args:
            report: The report dictionary from generate_report().

        Returns:
            A SOAP-formatted clinical note string.
        """
        r = report
        p = r["patient"]
        protocols = r["recommended_protocols"]
        today = r["generated_at"][:10]

        lines: List[str] = []
        lines.append("=" * 70)
        lines.append("CLINICAL NOTE — NEUROMODULATION PROTOCOL CONSULTATION")
        lines.append("=" * 70)
        lines.append(f"Date: {today}")
        lines.append(f"Report ID: {r['report_id']}")
        lines.append(f"Patient: {p['id']} | Age: {p['age']} | Sex: {p['sex']}")
        lines.append(f"Diagnosis: {p['diagnosis'].replace('_', ' ').title()}")
        if p.get("comorbidities"):
            lines.append(f"Comorbidities: {', '.join(p['comorbidities'])}")
        if p.get("medications"):
            lines.append(f"Current Medications: {', '.join(p['medications'])}")
        lines.append("-" * 70)
        lines.append("")

        # Subjective
        lines.append("S — SUBJECTIVE")
        lines.append("-" * 40)
        lines.append(
            f"Patient {p['id']} is a {p['age']}-year-old {p['sex']} with a diagnosis of "
            f"{p['diagnosis'].replace('_', ' ')} presenting for neuromodulation protocol "
            f"recommendation. "
        )
        if p.get("prior_treatments"):
            lines.append(
                f"Prior treatments include: {', '.join(p['prior_treatments'])}."
            )
        lines.append(
            "Protocol recommendations were generated using evidence-based "
            "neuromodulation guidelines."
        )
        lines.append("")

        # Objective
        lines.append("O — OBJECTIVE")
        lines.append("-" * 40)
        lines.append(f"Evidence Quality: {r['evidence_quality']}")
        lines.append(f"Data Sources Consulted: {r['data_sources_consulted']}")
        lines.append(f"Overall Confidence: {r['confidence_overall']:.0%}")
        if r["safety_warnings"]:
            lines.append("Safety Alerts:")
            for w in r["safety_warnings"]:
                lines.append(f"  - [{w['level'].upper()}] {w['message']}")
        else:
            lines.append("No automated safety alerts generated.")
        lines.append("")

        # Assessment
        lines.append("A — ASSESSMENT")
        lines.append("-" * 40)
        for i, proto in enumerate(protocols, 1):
            lines.append(
                f"{i}. {proto['protocol_name']} ({proto['modality']}) — "
                f"{proto.get('predicted_response', 'N/A')}"
            )
            params = proto["parameters"]
            param_strs = [f"{k}={v}" for k, v in params.items()]
            lines.append(f"   Params: {' | '.join(param_strs)}")
            if proto.get("evidence_summary"):
                lines.append(f"   Evidence: {proto['evidence_summary']}")
            if proto.get("contraindications_checked"):
                lines.append(f"   Contraindications: {proto['contraindications_checked']}")
            lines.append("")
        lines.append("")

        # Plan
        lines.append("P — PLAN")
        lines.append("-" * 40)
        for proto in protocols:
            lines.append(f"- Initiate {proto['protocol_name']}")
            if proto.get("total_duration") or proto["parameters"].get("total_duration"):
                dur = proto.get("total_duration") or proto["parameters"].get("total_duration")
                lines.append(f"  Duration: {dur}")
            lines.append(f"  Monitoring: Track symptom scores weekly; assess adverse effects.")
            lines.append(f"  Follow-up: Re-evaluate at completion; consider maintenance if response.")
            lines.append("")

