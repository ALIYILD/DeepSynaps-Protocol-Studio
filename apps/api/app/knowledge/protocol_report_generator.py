@dataclass
class StimulationParameters:
    """Structured stimulation parameters for a neuromodulation protocol."""
    montage: str
    current: Optional[str] = None  # e.g., "2.0 mA"
    frequency: Optional[str] = None  # e.g., "10 Hz"
    duration: Optional[str] = None  # e.g., "20 minutes"
    sessions: Optional[str] = None  # e.g., "15 (daily × 3 weeks)"
    total_duration: Optional[str] = None  # e.g., "3 weeks"
    pulse_count: Optional[str] = None  # e.g., "3000 pulses"
    wavelength: Optional[str] = None  # e.g., "810 nm" (for PBM)
    power_density: Optional[str] = None  # e.g., "250 mW/cm²"
    additional_params: Dict[str, str] = field(default_factory=dict)

    def to_dict(self) -> Dict[str, str]:
        """Convert to dictionary, excluding None values."""
        result = {
            "montage": self.montage,
        }
        for key in ["current", "frequency", "duration", "sessions",
                    "total_duration", "pulse_count", "wavelength", "power_density"]:
            value = getattr(self, key)
            if value is not None:
                result[key] = value
        result.update(self.additional_params)
        return result


