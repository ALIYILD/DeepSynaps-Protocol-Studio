    def _build_profile(self, patient_id: str, data: Dict[str, Any]) -> PatientProfile:
        """Build a PatientProfile from the input dictionary."""
        required = ["age", "sex", "diagnosis"]
        missing = [k for k in required if k not in data]
        if missing:
            raise InvalidInputError(f"Missing required profile fields: {missing}")

        return PatientProfile(
            patient_id=patient_id,
            age=int(data["age"]),
            sex=str(data["sex"]),
            diagnosis=str(data["diagnosis"]),
            comorbidities=list(data.get("comorbidities", [])),
            medications=list(data.get("medications", [])),
            prior_treatments=list(data.get("prior_treatments", [])),
            contraindications=list(data.get("contraindications", [])),
            special_population=data.get("special_population"),
        )

    def _generate_report_id(self, patient_id: str) -> str:
        """Generate a unique report ID."""
        date_str = datetime.utcnow().strftime("%Y%m%d")
        patient_suffix = patient_id.replace("-", "")
        return f"RPT-{date_str}-{patient_suffix}"

    def _build_recommendations(
        self,
        protocols: List[Dict[str, Any]],
        profile: PatientProfile,
    ) -> List[ProtocolRecommendation]:
        """
        Enrich raw protocols with evidence data and build
        ProtocolRecommendation objects.
        """
        recommendations: List[ProtocolRecommendation] = []

        for i, proto in enumerate(protocols, 1):
            modality = proto.get("modality", "tDCS")
            name = proto.get("protocol_name", f"{modality} Protocol")
            params_raw = proto.get("parameters", {})
            parameters = self._build_parameters(params_raw)

            # Look up evidence
            evidence_key = proto.get("evidence_key", "")
            if not evidence_key:
                evidence_key = self._infer_evidence_key(modality, name, profile.diagnosis)

            evidence = self._lookup_evidence(evidence_key)

            # Check contraindications
            contra_result = self._check_contraindications(modality, profile)

            # Build recommendation
            rec = ProtocolRecommendation(
                rank=i,
                modality=modality,
                protocol_name=name,
                parameters=parameters,
                evidence_summary=evidence.get("rct_summary", self._format_evidence(evidence)),
                predicted_response=evidence.get("predicted_response", "See evidence summary"),
                safety_summary=evidence.get("safety_summary", "Review safety literature."),
                contraindications_checked=contra_result,
                cost_estimate=self._format_cost(evidence.get("cost_estimate_usd")),
                key_references=list(evidence.get("key_refs", [])),
                evidence_grade=evidence.get("grade", EvidenceGrade.NA),
                response_confidence=evidence.get("response_confidence", 0.5),
                risk_level=self._assess_risk(modality, profile),
            )
            recommendations.append(rec)

        # Sort by response confidence descending
        recommendations.sort(key=lambda x: x.response_confidence, reverse=True)
        # Re-assign ranks
        for i, rec in enumerate(recommendations, 1):
            rec.rank = i

        return recommendations

    def _build_parameters(self, raw: Dict[str, Any]) -> StimulationParameters:
        """Build a StimulationParameters dataclass from a raw dict."""
        additional = {k: v for k, v in raw.items()
                      if k not in {"montage", "current", "frequency", "duration",
                                   "sessions", "total_duration", "pulse_count",
                                   "wavelength", "power_density"}}
        return StimulationParameters(
            montage=raw.get("montage", "Not specified"),
            current=raw.get("current"),
            frequency=raw.get("frequency"),
            duration=raw.get("duration"),
            sessions=raw.get("sessions"),
            total_duration=raw.get("total_duration"),
            pulse_count=raw.get("pulse_count"),
            wavelength=raw.get("wavelength"),
            power_density=raw.get("power_density"),
            additional_params=additional,
        )

    def _infer_evidence_key(
        self, modality: str, protocol_name: str, diagnosis: str
    ) -> str:
        """
        Infer the evidence database key from modality, protocol name, and diagnosis.
        """
        diagnosis_norm = diagnosis.lower().replace(" ", "_")
        mod_norm = modality.upper()

        # Direct mapping attempts
        if mod_norm == "TDCS" and "depress" in diagnosis_norm:
            if "f3" in protocol_name.lower() and "f4" in protocol_name.lower():
                return "tDCS_F3_F4_depression"
            if "f3" in protocol_name.lower() or "dlpfc" in protocol_name.lower():
                return "tDCS_F3_F4_depression"
        if mod_norm == "TDCS" and "anxiet" in diagnosis_norm:
            return "tDCS_F3_Fp2_gad"
        if mod_norm in {"TMS", "RTMS"} and "depress" in diagnosis_norm:
            if "bilateral" in protocol_name.lower():
                return "rtms_rdlpc_mdd_bilateral"
            return "rtms_ldlpc_depression"
        if mod_norm == "PBM":
            return "pbm_default"
        if mod_norm == "NEUROFEEDBACK":
            return "neurofeedback_default"

        # Fallback: try modality-only keys
        for key in self._evidence_db:
            if mod_norm.lower() in key.lower():
                return key

        return "tDCS_F3_F4_depression"  # ultimate fallback

    def _lookup_evidence(self, key: str) -> Dict[str, Any]:
        """Look up evidence data from the evidence database."""
        evidence = self._evidence_db.get(key, {})
        if not evidence:
            return {
                "rct_count": 0,
                "effect_size": 0.0,
                "p_value": "N/A",
                "grade": EvidenceGrade.NA,
                "key_refs": ["No evidence available — consult primary literature."],
                "predicted_response": "Insufficient evidence for prediction",
                "response_confidence": 0.0,
                "cost_estimate_usd": 0,
                "safety_summary": "Safety data not available.",
                "rct_summary": "No RCTs found",
            }

        # Build RCT summary string
        rct_count = evidence.get("rct_count", 0)
        es = evidence.get("effect_size", 0)
        pval = evidence.get("p_value", "N/A")
        nice = evidence.get("nicerecommended", False)

        rct_summary = f"{rct_count} RCTs"
        if es > 0:
            rct_summary += f", meta-analysis ES={es:.2f} (p{pval})"
        if nice:
            rct_summary += ", NICE recommended"

        result = dict(evidence)
        result["rct_summary"] = rct_summary
        return result

    def _format_evidence(self, evidence: Dict[str, Any]) -> str:
        """Format evidence summary string from evidence dict."""
        rct_count = evidence.get("rct_count", 0)
        es = evidence.get("effect_size", 0)
        if rct_count > 0:
            return f"{rct_count} RCTs, ES={es:.2f}"
        return "Evidence from clinical consensus"

    def _format_cost(self, cost_usd: Optional[int]) -> str:
        """Format cost estimate."""
        if cost_usd is None or cost_usd <= 0:
            return "Cost not estimated"
        return f"${cost_usd:,} USD"

    def _check_contraindications(self, modality: str, profile: PatientProfile) -> str:
        """Check patient profile against modality contraindications."""
        modality_contras = self._contra_db.get(modality, [])
        patient_contras = [c.lower() for c in profile.contraindications]

        if not modality_contras and not patient_contras:
            return "None identified"

        found = []
        for contra in modality_contras:
            for patient_contra in patient_contras:
                if patient_contra in contra.lower() or contra.lower() in patient_contra:
                    found.append(contra)
                    break

        if found:
            return f"ALERT: {', '.join(found)} — requires physician clearance"
        return "None identified (automated check only — clinical verification required)"

    def _check_safety(
        self,
        protocols: List[Dict[str, Any]],
        profile: PatientProfile,
    ) -> List[SafetyWarning]:
        """Generate safety warnings based on protocols and patient profile."""
        warnings: List[SafetyWarning] = []

        # Age-based warnings
        if profile.age < 12:
            warnings.append(SafetyWarning(
                level=RiskLevel.HIGH,
                message=(
                    "Very young pediatric patient — neuromodulation is generally "
                    "contraindicated under age 12. Exceptional circumstances require "
                    "specialist pediatric neuromodulation center approval."
                ),
                action_required="Pediatric neurologist and ethics board approval required.",
            ))
        elif profile.age < 18:
            warnings.append(SafetyWarning(
                level=RiskLevel.MODERATE,
                message=(
                    "Pediatric patient — neuromodulation protocols should only be "
                    "administered under specialist pediatric supervision. "
                    "Evidence base is limited in this population."
                ),
                action_required="Pediatric neurologist/psychiatrist approval required.",
            ))
        elif profile.age > 75:
            warnings.append(SafetyWarning(
                level=RiskLevel.LOW,
                message="Geriatric patient — consider reduced intensity and extended monitoring.",
                action_required="Geriatric assessment recommended prior to treatment.",
            ))

        # Pregnancy
        if profile.special_population == "pregnant":
            warnings.append(SafetyWarning(
                level=RiskLevel.MODERATE,
                message="Pregnancy — limited safety data for neuromodulation.",
                action_required="Obstetric and neurology consultation required.",
            ))

        # Device contraindications
        device_keywords = ["pacemaker", "dbst", "cochlear", "implant"]
        patient_contras_lower = [c.lower() for c in profile.contraindications]
        for keyword in device_keywords:
            if any(keyword in c for c in patient_contras_lower):
                warnings.append(SafetyWarning(
                    level=RiskLevel.CONTRAINDICATED,
                    message=f"Implanted {keyword} detected — electromagnetic stimulation may be contraindicated.",
                    action_required=f"Confirm device MRI-compatibility and obtain device specialist clearance.",
                ))

        # Seizure history (critical for TMS)
        if any("seizure" in c.lower() or "epilepsy" in c.lower() for c in patient_contras_lower):
            for proto in protocols:
                if proto.get("modality", "").upper() in {"TMS", "RTMS"}:
                    warnings.append(SafetyWarning(
                        level=RiskLevel.HIGH,
                        message=(
                            "Seizure history + TMS — seizure risk is elevated. "
                            "Alternative modalities (tDCS, PBM) should be considered."
                        ),
                        action_required="Epileptologist consultation required before TMS.",
                    ))

        # Medication warnings
        med_lower = [m.lower() for m in profile.medications]
        seizure_threshold_meds = ["bupropion", "tramadol", "clozapine", "theophylline"]
        for med in seizure_threshold_meds:
            if any(med in m for m in med_lower):
                warnings.append(SafetyWarning(
                    level=RiskLevel.MODERATE,
                    message=f"{med.title()} may lower seizure threshold — use caution with electromagnetic stimulation.",
                    action_required="Review medication list with prescribing physician.",
                ))

        return warnings

    def _assess_risk(self, modality: str, profile: PatientProfile) -> RiskLevel:
        """Assess overall risk level for a modality given the patient profile."""
        modality = modality.upper()

        # Check for contraindications
        contra_result = self._check_contraindications(modality, profile)
        if "ALERT" in contra_result:
            return RiskLevel.HIGH

        # Age-based risk
        if profile.age < 12:
            return RiskLevel.HIGH
        if profile.age < 18:
            return RiskLevel.MODERATE

        # Modality-specific base risk
        if modality in {"TMS", "RTMS"}:
            return RiskLevel.LOW
        if modality in {"TDCS", "TACS", "TRNS"}:
            return RiskLevel.LOW
        if modality == "PBM":
            return RiskLevel.LOW
        if modality == "NEUROFEEDBACK":
            return RiskLevel.NONE

        return RiskLevel.LOW

    def _calculate_evidence_quality(
        self, recommendations: List[ProtocolRecommendation]
    ) -> str:
        """Calculate overall evidence quality grade."""
        if not recommendations:
            return EvidenceGrade.NA.value

        # Use the best grade among recommendations
        grade_order = [EvidenceGrade.A, EvidenceGrade.B, EvidenceGrade.C,
                       EvidenceGrade.D, EvidenceGrade.NA]
        best_idx = min(grade_order.index(r.evidence_grade) for r in recommendations)
        return grade_order[best_idx].value

    def _calculate_overall_confidence(
        self, recommendations: List[ProtocolRecommendation]
    ) -> float:
        """Calculate overall confidence score (0-1)."""
        if not recommendations:
            return 0.0

        # Weight by rank (top protocols count more)
        weights = [1.0 / r.rank for r in recommendations]
        total_weight = sum(weights)
        weighted_conf = sum(r.response_confidence * w
                            for r, w in zip(recommendations, weights)) / total_weight

        # Cap at 0.95 since this is AI-assisted
        return min(weighted_conf, 0.95)

    def _count_data_sources(self) -> int:
        """Return the number of data sources consulted."""
        return len(set(self._evidence_db.keys()))

    def _apply_format(
        self, report: Dict[str, Any], fmt: ReportFormat
    ) -> Dict[str, Any]:
        """Apply format-specific transformations to the report."""
        if fmt == ReportFormat.FULL:
            return report

        if fmt == ReportFormat.SUMMARY:
            # Keep only top protocol
            if report["recommended_protocols"]:
                report["recommended_protocols"] = [report["recommended_protocols"][0]]
            # Reduce references
            for proto in report["recommended_protocols"]:
                proto["key_references"] = proto.get("key_references", [])[:2]
            return report

        if fmt == ReportFormat.PATIENT_FACING:
            # Simplify language
            report["disclaimer"] = PATIENT_FACING_DISCLAIMER
            report["research_only_notice"] = None

            # Translate jargon in protocols
            for proto in report["recommended_protocols"]:
                proto["protocol_name"] = self._simplify_text(proto["protocol_name"])
                proto["safety_summary"] = self._simplify_text(proto["safety_summary"])
                proto["evidence_summary"] = self._simplify_text(
                    proto.get("evidence_summary", "")
                )
                # Remove references and cost
                proto["key_references"] = []
                proto["cost_estimate"] = "Contact your clinic for cost information"
                proto["evidence_grade"] = ""
                # Simplify parameters
                params = proto.get("parameters", {})
                simplified_params = {}
                for k, v in params.items():
                    simple_key = self._simplify_text(k.replace("_", " "))
                    simple_val = self._simplify_text(str(v))
                    simplified_params[simple_key] = simple_val
                proto["parameters"] = simplified_params

            # Remove technical fields
            report["evidence_quality"] = ""
            report["confidence_overall"] = 0.0
            report["data_sources_consulted"] = 0
            report["research_only_notes"] = 0

            # Simplify safety warnings
            for w in report["safety_warnings"]:
                w["message"] = self._simplify_text(w["message"])
                if w.get("action_required"):
                    w["action_required"] = self._simplify_text(w["action_required"])

            return report

        return report

    def _simplify_text(self, text: str) -> str:
        """Replace medical jargon with patient-friendly terms."""
        if not text:
            return text
        result = text
        for jargon, friendly in PATIENT_FRIENDLY_TERMS.items():
            # Case-insensitive replacement
            pattern = re.compile(re.escape(jargon), re.IGNORECASE)
            result = pattern.sub(friendly, result)
        return result


