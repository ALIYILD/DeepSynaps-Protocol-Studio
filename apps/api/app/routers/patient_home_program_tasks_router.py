@router.post("/{task_id}/start", response_model=HomeworkStartOut)
def start_task(
    body: HomeworkStartIn = HomeworkStartIn(),
    task_id: str = Path(..., min_length=1, max_length=96),
    actor: AuthenticatedActor = Depends(get_authenticated_actor),
    db: Session = Depends(get_db_session),
) -> HomeworkStartOut:
    """Mark a task as started by the patient. Audit-only — no completion
    log here. The patient still has to log the actual completion via
    ``POST /api/v1/adherence/events`` (Adherence Events #350).

    Mutates ``task_json.patient_started_at`` so the clinician detail
    view can show "patient started this task at ...". Emits both a
    patient-side and clinician-visible mirror audit row.
    """
    patient = _resolve_patient_for_actor_hpt(db, actor)
    _assert_patient_consent_active(db, patient)
    is_demo = _patient_is_demo_hpt(db, patient)
    row = _resolve_task_or_404(db, patient, task_id)

    now = datetime.now(timezone.utc)
    task = load_task_dict(row.task_json)
    task["patient_started_at"] = now.isoformat()
    row.task_json = json.dumps(task, separators=(",", ":"), ensure_ascii=False)
    row.updated_at = now
    db.commit()

    _homework_audit(
        db,
        actor,
        event="task_started",
        target_id=row.id,
        note=(body.note or "")[:200],
        using_demo_data=is_demo,
    )
    # Clinician-visible mirror so the care-team feed shows the start
    # without the patient having to formally escalate.
    clinician_actor = patient.clinician_id or "actor-clinician-demo"
    _homework_audit(
        db,
        actor,
        event="task_started_to_clinician",
        target_id=clinician_actor,
        note=f"task={row.id}; server_task_id={row.server_task_id}",
        using_demo_data=is_demo,
        role_override="clinician",
        actor_override=clinician_actor,
    )

    return HomeworkStartOut(
        accepted=True, task_id=row.id, started_at=now.isoformat()
    )


@router.post(
    "/{task_id}/help-request",
    response_model=HomeworkHelpRequestOut,
    status_code=201,
)
def help_request(
    body: HomeworkHelpRequestIn,
    task_id: str = Path(..., min_length=1, max_length=96),
    actor: AuthenticatedActor = Depends(get_authenticated_actor),
    db: Session = Depends(get_db_session),
) -> HomeworkHelpRequestOut:
    """Open a Patient Messages thread linked to this task.

    The thread is created with ``thread_id = "task-{task_id}"`` so the
    patient inbox can deep-link straight to the help-request thread
    (mirror of the report-question pattern from #346/#347). Creates a
    ``Message`` row whose ``patient_id`` is the patient and whose
    ``recipient_id`` is the clinician. If ``is_urgent`` is set, the
    ``priority`` column is ``urgent`` and a HIGH-priority
    clinician-visible audit row is emitted.
    """
    patient = _resolve_patient_for_actor_hpt(db, actor)
    _assert_patient_consent_active(db, patient)
    is_demo = _patient_is_demo_hpt(db, patient)
    row = _resolve_task_or_404(db, patient, task_id)

    recipient = patient.clinician_id or "actor-clinician-demo"
    thread_id = f"task-{row.id}"
    msg_id = str(uuid.uuid4())
    task = load_task_dict(row.task_json)
    title_text = (
        task.get("title") or task.get("name") or "home program task"
    )
    subject = f"Help with: {str(title_text)[:120]}"
    full_body_parts = [body.reason.strip()]
    if body.body:
        full_body_parts.append(body.body.strip())
    full_body = "\n\n".join(full_body_parts)[:4000]

    msg = Message(
        id=msg_id,
        sender_id=actor.actor_id,
        recipient_id=recipient,
        patient_id=patient.id,
        body=full_body,
        subject=subject[:255],
        category="task-help",
        thread_id=thread_id,
        priority=("urgent" if body.is_urgent else "normal"),
        created_at=datetime.now(timezone.utc),
    )
    db.add(msg)
    db.commit()

    _homework_audit(
        db,
        actor,
        event="task_help_requested",
        target_id=row.id,
        note=(
            f"thread={thread_id}; recipient={recipient}; "
            f"chars={len(body.reason)}; urgent={1 if body.is_urgent else 0}"
        ),
        using_demo_data=is_demo,
    )
    if body.is_urgent:
        _homework_audit(
            db,
            actor,
            event="task_help_urgent_to_clinician",
            target_id=recipient,
            note=(
                f"priority=high; thread={thread_id}; task={row.id}"
            ),
            using_demo_data=is_demo,
            role_override="clinician",
            actor_override=recipient,
        )

    return HomeworkHelpRequestOut(
        accepted=True,
        task_id=row.id,
        thread_id=thread_id,
        message_id=msg_id,
        is_urgent=bool(body.is_urgent),
    )
