_DEMO_PATIENT_ACTOR_ID = "actor-patient-demo"
_DEMO_PATIENT_EMAILS = {"patient@deepsynaps.com", "patient@demo.com"}

_DATE_RE = re.compile(r"^\d{4}-\d{2}-\d{2}$")
_EXTERNAL_ID_RE = re.compile(r"^[A-Za-z0-9._-]{1,96}$")


def _aware(dt: Optional[datetime]) -> Optional[datetime]:
    """Coerce a naive datetime to tz-aware UTC.

    SQLite strips tzinfo on roundtrip — see memory note
    ``deepsynaps-sqlite-tz-naive.md``. All comparisons against
    ``datetime.now(timezone.utc)`` must coerce first.
    """
    if dt is None:
        return None
    return dt if dt.tzinfo is not None else dt.replace(tzinfo=timezone.utc)


def _iso(dt: Optional[datetime]) -> Optional[str]:
    aw = _aware(dt)
    return aw.isoformat() if aw is not None else None


def _patient_is_demo_hpt(db: Session, patient: Patient | None) -> bool:
    """Mirrors :func:`patients_router._patient_is_demo` for this surface."""
    if patient is None:
        return False
    notes = patient.notes or ""
    if notes.startswith("[DEMO]"):
        return True
    try:
        u = db.query(User).filter_by(id=patient.clinician_id).first()
        if u is None or not u.clinic_id:
            return False
        return u.clinic_id in {"clinic-demo-default", "clinic-cd-demo"}
    except Exception:
        return False


def _resolve_patient_for_actor_hpt(
    db: Session, actor: AuthenticatedActor
) -> Patient:
    """Return the Patient row the actor is allowed to act on.

    Patient role only. Cross-role hits return 404 (never 403/401) so the
    patient-scope URL existence is invisible to clinicians and admins.
    """
    if actor.role != "patient":
        raise ApiServiceError(
            code="not_found",
            message="Patient record not found.",
            status_code=404,
        )

    if actor.actor_id == _DEMO_PATIENT_ACTOR_ID:
        patient = (
            db.query(Patient)
            .filter(Patient.email.in_(list(_DEMO_PATIENT_EMAILS)))
            .first()
        )
    else:
        user = db.query(User).filter_by(id=actor.actor_id).first()
        if user is None or not user.email:
            raise ApiServiceError(
                code="not_found",
                message="Patient record not found.",
                status_code=404,
            )
        patient = db.query(Patient).filter(Patient.email == user.email).first()
    if patient is None:
        raise ApiServiceError(
            code="not_found",
            message="Patient record not found.",
            status_code=404,
        )
    return patient


def _consent_active_hpt(db: Session, patient: Patient) -> bool:
    """Same consent gate as adherence_events / patient_messages / wellness_hub."""
    has_withdrawn = (
        db.query(ConsentRecord)
        .filter(
            ConsentRecord.patient_id == patient.id,
            ConsentRecord.status == "withdrawn",
        )
        .first()
        is not None
    )
    if has_withdrawn:
        return False
    if patient.consent_signed:
        return True
    has_active = (
        db.query(ConsentRecord)
        .filter(
            ConsentRecord.patient_id == patient.id,
            ConsentRecord.status == "active",
        )
        .first()
        is not None
    )
    return has_active


def _assert_patient_consent_active(db: Session, patient: Patient) -> None:
    if not _consent_active_hpt(db, patient):
        raise ApiServiceError(
            code="consent_inactive",
            message=(
                "Starting a task or sending a help request requires "
                "active consent. Existing tasks remain readable until "
                "consent is reinstated."
            ),
            status_code=403,
        )


def _homework_audit(
    db: Session,
    actor: AuthenticatedActor,
    *,
    event: str,
    target_id: str,
    note: str = "",
    using_demo_data: bool = False,
    target_type: str = "home_program_tasks",
    role_override: Optional[str] = None,
    actor_override: Optional[str] = None,
) -> str:
    """Best-effort audit hook for the ``home_program_tasks`` surface.

    Never raises — audit must not block the UI even when the umbrella
    audit table is unreachable. Mirrors helpers in
    adherence_events_router / home_devices_patient_router /
    patient_messages_router / wellness_hub_router.
    """
    from app.repositories.audit import create_audit_event  # noqa: PLC0415

    now = datetime.now(timezone.utc)
    role = role_override or actor.role
    actor_id = actor_override or actor.actor_id
    event_id = (
        f"home_program_tasks-{event}-{actor_id}-{int(now.timestamp())}"
        f"-{uuid.uuid4().hex[:6]}"
    )
    note_parts: list[str] = []
    if using_demo_data:
        note_parts.append("DEMO")
    if note:
        note_parts.append(note[:500])
    final_note = "; ".join(note_parts) or event
    try:
        create_audit_event(
            db,
            event_id=event_id,
            target_id=str(target_id) or actor_id,
            target_type=target_type,
            action=f"home_program_tasks.{event}",
            role=role,
            actor_id=actor_id,
            note=final_note[:1024],
            created_at=now.isoformat(),
        )
    except Exception:  # pragma: no cover — audit must never block UI
        _log.exception("home_program_tasks self-audit skipped")
    return event_id


def _validate_external_task_id(task_id: str) -> None:
    if not _EXTERNAL_ID_RE.match(task_id or ""):
        raise ApiServiceError(
            code="not_found",
            message="Task not found.",
            status_code=404,
        )


def _resolve_task_or_404(
    db: Session, patient: Patient, task_id: str
) -> ClinicianHomeProgramTask:
    """Return the home program task owned by this patient, or 404.

    Cross-patient lookups return 404 even if the row exists — the
    existence of that row must not be observable.
    """
    _validate_external_task_id(task_id)
    row = session_get_task(db, task_id)
    if row is None or row.patient_id != patient.id:
        raise ApiServiceError(
            code="not_found",
            message="Task not found.",
            status_code=404,
        )
    return row


def session_get_task(db: Session, task_id: str) -> Optional[ClinicianHomeProgramTask]:
    return (
        db.query(ClinicianHomeProgramTask)
        .filter(ClinicianHomeProgramTask.id == task_id)
        .first()
    )


def _completion_for(
    db: Session, patient_id: str, server_task_id: str
) -> Optional[PatientHomeProgramTaskCompletion]:
    return (
        db.query(PatientHomeProgramTaskCompletion)
        .filter(
            PatientHomeProgramTaskCompletion.patient_id == patient_id,
            PatientHomeProgramTaskCompletion.server_task_id == server_task_id,
        )
        .order_by(PatientHomeProgramTaskCompletion.completed_at.desc())
        .first()
    )


def _safe_dict(value: Any) -> dict[str, Any]:
    if isinstance(value, dict):
        return value
    return {}


def _patient_safe_task_view(
    row: ClinicianHomeProgramTask,
    *,
    completion: Optional[PatientHomeProgramTaskCompletion] = None,
) -> dict[str, Any]:
    """Build a patient-safe projection for the task drawer / list.

    Strips clinician-only confidence tiers / scores. Surfaces only
    fields the patient should see: title, instructions, illustration,
    equipment, due_on, time_bucket, repeat, clinician-authored
    rationale ("why am I doing this?") and clinician-authored
    instructions / notes. NEVER returns AI-generated rationale —
    only fields the clinician explicitly authored or the catalog
    template provided.
    """
    task = load_task_dict(row.task_json)
    safe = {
        "id": row.id,
        "server_task_id": row.server_task_id,
        "patient_id": row.patient_id,
        "title": task.get("title") or task.get("name") or "Home program task",
        "category": task.get("category"),
        "task_type": task.get("task_type") or task.get("type"),
        "duration_min": task.get("duration_min") or task.get("durationMin"),
        "repeat": task.get("repeat") or task.get("repeat_pattern"),
        "due_on": task.get("due_on") or task.get("dueOn"),
        "due_by": task.get("due_by") or task.get("dueBy"),
        "time_bucket": task.get("time_bucket") or task.get("timeBucket"),
        "instructions": task.get("instructions") or task.get("description") or "",
        "illustration_url": task.get("illustration_url") or task.get("illustrationUrl"),
        "equipment": task.get("equipment") or [],
        "rationale": task.get("rationale") or task.get("why"),
        "rationale_author": task.get("rationale_author") or task.get("clinician_assigned_by"),
        "clinician_note": _safe_dict(task.get("clinician_note") or task.get("clinicianNote")),
        "assigned_at": _iso(row.created_at),
        "updated_at": _iso(row.updated_at),
        "started_at": None,
        "completed": False,
        "completed_at": None,
        "skipped": False,
        "rating": None,
        "difficulty": None,
        "feedback_text": None,
    }
    if completion is not None:
        safe["completed"] = bool(completion.completed)
        safe["completed_at"] = _iso(completion.completed_at)
        safe["rating"] = completion.rating
        safe["difficulty"] = completion.difficulty
        safe["feedback_text"] = completion.feedback_text
    # The "started_at" stamp lives on the task_json — if the patient
    # earlier hit /start, we mirrored a structured field there.
    started_at = task.get("patient_started_at")
    if isinstance(started_at, str):
        safe["started_at"] = started_at
    return safe


def _list_tasks_for_patient(
    db: Session, patient: Patient
) -> list[ClinicianHomeProgramTask]:
    return (
        db.query(ClinicianHomeProgramTask)
        .filter(ClinicianHomeProgramTask.patient_id == patient.id)
        .order_by(ClinicianHomeProgramTask.created_at.desc())
        .limit(500)
        .all()
    )


