@router.get("/today", response_model=HomeworkListResponse)
def list_today(
    actor: AuthenticatedActor = Depends(get_authenticated_actor),
    db: Session = Depends(get_db_session),
) -> HomeworkListResponse:
    """List the patient's tasks due today (not yet completed)."""
    patient = _resolve_patient_for_actor_hpt(db, actor)
    is_demo = _patient_is_demo_hpt(db, patient)
    today_str = datetime.now(timezone.utc).date().isoformat()

    rows = _list_tasks_for_patient(db, patient)
    items: list[PatientHomeworkTaskOut] = []
    for row in rows:
        task = load_task_dict(row.task_json)
        due = (task.get("due_on") or task.get("dueOn") or "")[:10]
        if due != today_str:
            continue
        completion = _completion_for(db, patient.id, row.server_task_id)
        if completion is not None and bool(completion.completed):
            continue
        items.append(
            PatientHomeworkTaskOut(**_patient_safe_task_view(row, completion=completion))
        )

    _homework_audit(
        db,
        actor,
        event="today_viewed",
        target_id=patient.id,
        note=f"items={len(items)}",
        using_demo_data=is_demo,
    )

    return HomeworkListResponse(
        items=items,
        total=len(items),
        consent_active=_consent_active_hpt(db, patient),
        is_demo=is_demo,
    )


@router.get("/upcoming", response_model=HomeworkListResponse)
def list_upcoming(
    days: int = Query(default=7, ge=1, le=30),
    actor: AuthenticatedActor = Depends(get_authenticated_actor),
    db: Session = Depends(get_db_session),
) -> HomeworkListResponse:
    """List the patient's tasks due in the next N days (excludes today)."""
    patient = _resolve_patient_for_actor_hpt(db, actor)
    is_demo = _patient_is_demo_hpt(db, patient)
    today = datetime.now(timezone.utc).date()
    horizon = today + timedelta(days=days)

    rows = _list_tasks_for_patient(db, patient)
    items: list[PatientHomeworkTaskOut] = []
    for row in rows:
        task = load_task_dict(row.task_json)
        due_str = (task.get("due_on") or task.get("dueOn") or "")[:10]
        if not _DATE_RE.match(due_str):
            continue
        try:
            due_date = date.fromisoformat(due_str)
        except ValueError:
            continue
        if due_date <= today or due_date > horizon:
            continue
        completion = _completion_for(db, patient.id, row.server_task_id)
        items.append(
            PatientHomeworkTaskOut(**_patient_safe_task_view(row, completion=completion))
        )

    _homework_audit(
        db,
        actor,
        event="upcoming_viewed",
        target_id=patient.id,
        note=f"items={len(items)} days={days}",
        using_demo_data=is_demo,
    )

    return HomeworkListResponse(
        items=items,
        total=len(items),
        consent_active=_consent_active_hpt(db, patient),
        is_demo=is_demo,
    )


@router.get("/completed", response_model=HomeworkListResponse)
def list_completed(
    since: Optional[str] = Query(default=None, max_length=10),
    actor: AuthenticatedActor = Depends(get_authenticated_actor),
    db: Session = Depends(get_db_session),
) -> HomeworkListResponse:
    """List the patient's completed tasks (newest first; max 200)."""
    patient = _resolve_patient_for_actor_hpt(db, actor)
    is_demo = _patient_is_demo_hpt(db, patient)

    completions_q = (
        db.query(PatientHomeProgramTaskCompletion)
        .filter(
            PatientHomeProgramTaskCompletion.patient_id == patient.id,
            PatientHomeProgramTaskCompletion.completed.is_(True),
        )
        .order_by(PatientHomeProgramTaskCompletion.completed_at.desc())
        .limit(200)
    )
    if since and _DATE_RE.match(since):
        try:
            since_dt = datetime.fromisoformat(f"{since}T00:00:00+00:00")
            completions_q = completions_q.filter(
                PatientHomeProgramTaskCompletion.completed_at >= since_dt
            )
        except ValueError:
            pass

    completions = completions_q.all()
    items: list[PatientHomeworkTaskOut] = []
    for c in completions:
        row = (
            db.query(ClinicianHomeProgramTask)
            .filter(ClinicianHomeProgramTask.server_task_id == c.server_task_id)
            .first()
        )
        if row is None or row.patient_id != patient.id:
            continue
        items.append(
            PatientHomeworkTaskOut(**_patient_safe_task_view(row, completion=c))
        )

    _homework_audit(
        db,
        actor,
        event="completed_viewed",
        target_id=patient.id,
        note=f"items={len(items)} since={since or '-'}",
        using_demo_data=is_demo,
    )

    return HomeworkListResponse(
        items=items,
        total=len(items),
        consent_active=_consent_active_hpt(db, patient),
        is_demo=is_demo,
    )


@router.get("/summary", response_model=HomeworkSummaryResponse)
def get_summary(
    actor: AuthenticatedActor = Depends(get_authenticated_actor),
    db: Session = Depends(get_db_session),
) -> HomeworkSummaryResponse:
    """Top counts: assigned-today, due-today, overdue, completion_rate_7d, missed_streak."""
    patient = _resolve_patient_for_actor_hpt(db, actor)
    is_demo = _patient_is_demo_hpt(db, patient)

    rows = _list_tasks_for_patient(db, patient)
    now = datetime.now(timezone.utc)
    today = now.date()
    today_str = today.isoformat()
    week_cutoff = now - timedelta(days=7)

    # Build a quick set of completed server_task_ids to avoid N queries.
    completion_rows = (
        db.query(PatientHomeProgramTaskCompletion)
        .filter(
            PatientHomeProgramTaskCompletion.patient_id == patient.id,
            PatientHomeProgramTaskCompletion.completed.is_(True),
        )
        .all()
    )
    completed_by_server_id: dict[str, PatientHomeProgramTaskCompletion] = {}
    for c in completion_rows:
        existing = completed_by_server_id.get(c.server_task_id)
        if existing is None or _aware(c.completed_at) > _aware(existing.completed_at):  # type: ignore[operator]
            completed_by_server_id[c.server_task_id] = c

    assigned_today = 0
    due_today = 0
    overdue = 0
    completed_today = 0
    completed_7d = 0

    for row in rows:
        created = _aware(row.created_at)
        if created is not None and created.date() == today:
            assigned_today += 1
        task = load_task_dict(row.task_json)
        due_str = (task.get("due_on") or task.get("dueOn") or "")[:10]
        completion = completed_by_server_id.get(row.server_task_id)
        if completion is None and _DATE_RE.match(due_str):
            try:
                due_date = date.fromisoformat(due_str)
            except ValueError:
                due_date = None
            if due_date == today:
                due_today += 1
            elif due_date is not None and due_date < today:
                overdue += 1
        if completion is not None:
            completed_at_aw = _aware(completion.completed_at)
            if completed_at_aw is not None:
                if completed_at_aw.date() == today:
                    completed_today += 1
                if completed_at_aw >= week_cutoff:
                    completed_7d += 1

    # Completion rate over last 7d:
    #   numerator   = completed in last 7d
    #   denominator = tasks with due_on in last 7d (cap completed_7d when no due dates)
    seven_days_ago = today - timedelta(days=7)
    due_in_7d = 0
    for row in rows:
        task = load_task_dict(row.task_json)
        due_str = (task.get("due_on") or task.get("dueOn") or "")[:10]
        if not _DATE_RE.match(due_str):
            continue
        try:
            due_date = date.fromisoformat(due_str)
        except ValueError:
            continue
        if seven_days_ago <= due_date <= today:
            due_in_7d += 1
    if due_in_7d == 0:
        completion_rate_7d = 100 if completed_7d > 0 else 0
    else:
        completion_rate_7d = min(100, int(round(completed_7d * 100 / due_in_7d)))

    # Missed streak: count days back from yesterday with at least one
    # task due that wasn't completed.
    days_with_complete: set[str] = set()
    for c in completion_rows:
        completed_at_aw = _aware(c.completed_at)
        if completed_at_aw is not None:
            days_with_complete.add(completed_at_aw.date().isoformat())
    days_with_due: dict[str, int] = {}
    for row in rows:
        task = load_task_dict(row.task_json)
        due_str = (task.get("due_on") or task.get("dueOn") or "")[:10]
        if _DATE_RE.match(due_str):
            days_with_due[due_str] = days_with_due.get(due_str, 0) + 1

    missed_streak = 0
    cursor = today
    safety = 0
    while safety < 365:
        cursor_iso = cursor.isoformat()
        if cursor_iso in days_with_complete:
            break
        if days_with_due.get(cursor_iso, 0) > 0:
            missed_streak += 1
        else:
            # No tasks were due that day; not a "missed" day. Stop the
            # streak rather than over-counting.
            if missed_streak > 0:
                break
        cursor = cursor - timedelta(days=1)
        safety += 1

    last_completed_at = None
    if completion_rows:
        latest = max(completion_rows, key=lambda c: _aware(c.completed_at) or datetime.min.replace(tzinfo=timezone.utc))
        last_completed_at = _iso(latest.completed_at)

    _homework_audit(
        db,
        actor,
        event="summary_viewed",
        target_id=patient.id,
        note=(
            f"assigned_today={assigned_today} due_today={due_today} "
            f"overdue={overdue} completed_today={completed_today} "
            f"completed_7d={completed_7d} rate_7d={completion_rate_7d} "
            f"missed_streak={missed_streak}"
        ),
        using_demo_data=is_demo,
    )

    return HomeworkSummaryResponse(
        total_assigned=len(rows),
        assigned_today=assigned_today,
        due_today=due_today,
        overdue=overdue,
        completed_today=completed_today,
        completed_7d=completed_7d,
        completion_rate_7d=completion_rate_7d,
        missed_streak_days=missed_streak,
        last_completed_at=last_completed_at,
        consent_active=_consent_active_hpt(db, patient),
        is_demo=is_demo,
    )


