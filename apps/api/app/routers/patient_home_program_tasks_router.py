"""Patient Home Program Tasks (Homework) launch-audit (2026-05-01).

Seventh patient-facing launch-audit surface in the chain after Symptom
Journal (#344), Wellness Hub (#345), Patient Reports (#346), Patient
Messages (#347), Home Devices (#348) and Adherence Events (#350).

Closes the home-therapy regulator loop end-to-end: clinician assigns
tasks via the clinician-side ``home_program_tasks_router`` → patient
SEES tasks here → patient LOGS completion via the Adherence Events
router (#350) → side-effect with severity >= 7 escalates to the AE Hub
(#342) → safety review in QA Hub (#321).

This page is read-side + start + help-request. **Task completion is
NOT logged here** — the "Log now" CTA deep-links the patient into
``/?page=pt-adherence-events&task_id=<id>`` so completion flows
through the single-source-of-truth ``patient_adherence_events`` queue.

Endpoints
---------
GET    /api/v1/home-program-tasks/patient/today          Today's tasks (due today, not yet completed)
GET    /api/v1/home-program-tasks/patient/upcoming       Next 7d (excludes today)
GET    /api/v1/home-program-tasks/patient/completed      Completed history (limit 200)
GET    /api/v1/home-program-tasks/patient/summary        Counts: assigned-today, due-today, overdue, completion_rate_7d, missed_streak
GET    /api/v1/home-program-tasks/patient/{task_id}      Detail (404 cross-patient)
POST   /api/v1/home-program-tasks/patient/{task_id}/start          Mark started — emits clinician-visible audit
POST   /api/v1/home-program-tasks/patient/{task_id}/help-request   Creates Patient Messages thread linked to task_id
GET    /api/v1/home-program-tasks/patient/export.csv      DEMO-prefixed when demo
GET    /api/v1/home-program-tasks/patient/export.ndjson   DEMO-prefixed when demo
POST   /api/v1/home-program-tasks/patient/audit-events    Page-level audit ingestion (target_type=home_program_tasks)

Role gate
---------
Patient role only on these endpoints. Clinicians use the existing
clinician-side ``home_program_tasks_router`` queue. Cross-role hits
return 404 (never 403/401) so the patient-scope URL existence is
invisible to clinicians and admins. Cross-patient task lookups also
return 404.

Consent gate
------------
Once a patient has revoked consent
(``Patient.consent_signed = False`` OR a ``ConsentRecord`` row with
``status='withdrawn'``) the page is read-only post-revocation:
existing tasks remain visible, but no new ``start`` or
``help-request`` events can be written (HTTP 403). Mirrors the
adherence_events / patient_messages / wellness_hub / home_devices
pattern.

Demo honesty
------------
``is_demo`` is sourced from :func:`_patient_is_demo_hpt`. Exports
prefix ``DEMO-`` to the filename whenever the patient row is demo
and a ``X-HomeProgramTasks-Demo: 1`` response header is set.
"""

