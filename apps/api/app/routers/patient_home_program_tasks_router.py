@router.post("/audit-events", response_model=HomeworkAuditEventOut)
def post_homework_audit_event(
    body: HomeworkAuditEventIn,
    actor: AuthenticatedActor = Depends(get_authenticated_actor),
    db: Session = Depends(get_db_session),
) -> HomeworkAuditEventOut:
    """Page-level audit ingestion for the patient Homework UI.

    Surface: ``home_program_tasks``. Common events: ``view`` (mount),
    ``filter_changed``, ``task_viewed``, ``task_started``,
    ``task_completed_via_adherence``, ``task_skipped_via_adherence``,
    ``task_help_requested``, ``export``, ``deep_link_followed``,
    ``demo_banner_shown``, ``consent_banner_shown``.

    Patient role only — clinicians cannot emit ``home_program_tasks``
    audit rows directly. Cross-patient ingestion is blocked because
    ``task_id`` (when supplied) is verified to belong to the actor's
    patient.
    """
    if actor.role != "patient":
        raise ApiServiceError(
            code="patient_role_required",
            message=(
                "Home Program Tasks audit ingestion is restricted to "
                "the patient role."
            ),
            status_code=403,
        )
    patient = _resolve_patient_for_actor_hpt(db, actor)
    is_demo = _patient_is_demo_hpt(db, patient)

    target_id: str = patient.id
    if body.task_id:
        any_row = (
            db.query(ClinicianHomeProgramTask)
            .filter(ClinicianHomeProgramTask.id == body.task_id)
            .first()
        )
        if any_row is not None and any_row.patient_id != patient.id:
            raise ApiServiceError(
                code="not_found",
                message="Task not found.",
                status_code=404,
            )
        target_id = body.task_id

    note_parts: list[str] = []
    if body.task_id:
        note_parts.append(f"task={body.task_id}")
    if body.note:
        note_parts.append(body.note[:480])
    note = "; ".join(note_parts) or body.event

    event_id = _homework_audit(
        db,
        actor,
        event=body.event,
        target_id=target_id,
        note=note,
        using_demo_data=bool(body.using_demo_data) or is_demo,
    )
    return HomeworkAuditEventOut(accepted=True, event_id=event_id)


