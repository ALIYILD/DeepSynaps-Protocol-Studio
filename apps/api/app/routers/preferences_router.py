"""Preferences router — /api/v1/preferences.

Two concerns living behind a single prefix:

1. Per-user UI + notification preferences (``user_preferences`` table):
   language, date/time format, units, notification matrix (channel x event),
   quiet hours, digest cadence, reminder timing, auto-logout, telemetry opt-ins.

2. Per-clinic clinical defaults (``clinic_defaults`` table): default protocol,
   session duration, follow-up cadence, course length, consent template,
   disclaimer, default assessment battery, AE handling protocol.

Endpoints:
  GET   /api/v1/preferences                     -> UserPreferencesResponse
  PATCH /api/v1/preferences                     -> UserPreferencesResponse
  GET   /api/v1/preferences/clinical-defaults   -> ClinicDefaultsResponse
  PATCH /api/v1/preferences/clinical-defaults   -> ClinicDefaultsResponse  (admin)

PATCH semantics: partial update. For JSON-typed columns
(``notification_prefs``, ``quiet_hours``, ``reminder_timing``,
``default_assessments``) the whole blob is replaced when the body provides a
value (no deep-merge in v0 — too error-prone for nested matrices).

See apps/api/SETTINGS_API_DESIGN.md - Preferences router for the full spec.
"""
